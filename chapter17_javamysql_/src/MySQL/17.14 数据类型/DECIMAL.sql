-- 演示decimal类型、float、double使用
CREATE TABLE `t06` (
	num1 FLOAT,
	num2 DOUBLE,
	num3 DECIMAL(30,20)
);
INSERT INTO `t06` VALUES (88.12345678912345, 88.12345678912345, 88.12345678912345);
-- 最后一个DECIMAL类型自动补0
SELECT * FROM `t06`;

CREATE TABLE `t07` (num DECIMAL(65))；
INSERT INTO t07 VALUES (8912345678987654321876543456789);
SELECT * FROM t07;

CREATE TABLE `t08` (num BIGINT UNSIGNED);
INSERT INTO `t08` VALUES (8912345678987654321876543456789);

-- DECIMAL[M,D] [UNSIGNED] 
-- 可以支持更加精确的小数位。M是小数位数(精度)的总数，D是小数点(标度)后面的位数。
-- M就相当于是这个数总共的位数，D是相当于小数点后面的位数
-- 如果D是0，则值没有小数点或分数部份。M最大65，D最大30。如果D被省略，默认是0.如果M被省略，默认是10.