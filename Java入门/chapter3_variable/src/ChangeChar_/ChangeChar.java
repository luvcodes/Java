package ChangeChar_;

public class ChangeChar {
    public static void main(String[] args) {
        // \t，一个制表位，实现对齐功能
        System.out.println("天津\t北京\t上海\t" );
        // \n, 换行符
        System.out.println("John \nSmith");
        // \\, 在一行里输出一个\
        System.out.println("hello\\ryan");
        // \"，输出一个"
        System.out.println("Ryan said: \"you shoudld learn java. \"");
        System.out.println("Ryan said: \'you should learn java. \'");
        // \r，一个回车
        System.out.println("Enter\rbackspace");
        System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
    }
}
